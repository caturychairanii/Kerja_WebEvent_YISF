                <h2>MTsN 6 Sleman Raih 4 Medali Emas dan 1 Special Award YISF</h2>

                <div class="scroll">
                <p>
                <b>Sleman (MTsN 6 Sleman)</b>—Dengan tekad dan percaya diri yang kuat, 4 tim KIR MTsN 6 Sleman mengikuti lomba penelitian remaja bernama Youth International Science Fair (YISF) pada 10 s.d. 12 April 2021. Kegigihan keempat tim tersebut berbuah manis dengan didapatkan nya 4 medali emas dan sebuah special award, Sabtu (17/4/2021)                </p>

                <p>
                <b>“Youth International Science Fair</b> adalah salah satu event yang diselenggarakan oleh Indonesian Youth Scientist Association (IYSA) yaitu suatu lembaga yang bergerak dalam bidang inovasi dan invesi. Tujuannya adalah untuk menumbuhkan minat para saintis muda menemukan inovasi dan invasi baru melalui penelitian remaja yang nantinya berguna bagi kehidupan,” jelas Sri Dwi Astuti koordinator program sains madrasah.                </p>

                <p>
                Harsoyo Kepala MTsN 6 Sleman menjelaskan bahwa ajang YISF ini diikuti oleh berbagai negara seperti Thailand, Turkey, USA, Russia, Indonesia, Malaysia dan beberapa negara lain. Tim MTsN 6 Sleman ini harus berjuang keras bersaing dengan negara-negara lainnya untuk mendapatkan hasil terbaik. Dimentori oleh para pembimbing KIR, tim MTsN 6 Sleman berusaha untuk membuat suatu penelitian yang dirasa diperlukan untuk kondisi saat ini. Salah satu penelitian yang termasuk dalam kategori Life Science berjudul “A Combination of Egg Shell, Banana Peel, and Rice Washing Water Waste as Liquid Fertitizer” berhasil menarik perhatian juri dan mampu menyabet ‘Gold Medal’.                </p>

                <p>
                “Berbagai kejuaraan yang diraih oleh MTsN 6 baik ditingkat Nasinal, ASEAN, bahkan Internasional  merupakan sebuah penegasan bahwa madrasah siap berkompetisi dengan lembaga pendidikan lainnya,” ungkap Harsoyo.                </p>

                <p>
                Penelitian sederhana tentang pupuk cair dengan bahan utama kulit pisang, cangkang telur dan air cucian beras ini dinilai sangat relevan dengan keadaan saat ini. Kualitas pertanian di Indonesia bahkan di dunia saati ini mengalami penurunan secara signifikan seiring dengan semakin tidak produktifnya lahan pertanian akibat penggunaan pupuk kimia dan kurangnya rehabilitasi tanah itu sendiri. menurut salah satu tim KIR M. Rayyan Atthariq, penelitian ini bertujuan untuk menghasilan pupuk organik yang mampu mengembalikan kondisi tanah menjadi subur kembali karena ketiga bahan tersebut telah dikenal memiliki kandungan nitrogen, phospar, kalium dan karbon yang merupakan unsur hara yang dibutuhkan oleh tanaman.                </p>

                <p>
                Karya tim MTsN 6 Sleman lain yang juga membuat juri menganugerahkan sebuah medali emas bahkan special award adalah peelitian di kategori technology and computer secondary yang berjudul “Development of a National boundary marker as a practical solution in maintaining the territorial integrity based on renewable energy.” Ordelia Austrin salah satu tim ini menjelaskan bahwa penelitian ini bertujuan untuk merancang penanda batas negara yang efektif, untuk mengetahui cara kerja penanda batas nasional berbasis energi terbarukan bekerja dalam mempertahankan negara wilayah dan untuk menentukan kelayakan penanda batas nasional tersebut. Sebuah penelitian yang bertujuan sangat mulia dan sangat berguna bagi sistem pertahanan negara.                </p>

                <p>
                Dua lagi karya tim MTsN 6 Sleman yang meraih ‘Gold Medal’ adalah "ASTRO" Development of Muscle Signal-Based Arm Robot As a Solution for Post-Stroke Rehabilitation” dan “phosphane stick ( a smart stick for assisting people with visual impairment in orientation and mobility, direction detection, and social distancing during Covid-19 pandemic” yang keduanya masuk dalam kategory “physics and engineering”. Semoga kemengan ini menjadi pemicu bagi siswa-siswi MTsN 6 Sleman untuk membuat suatu penelian yang hasilnya dapat disumbangkan bagi kemaslahatan alam seisinya. (luf)                
